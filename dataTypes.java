class dataTypes {
	public static void main(String[] args){
	System.out.println("Datatypes examples");
	byte x = 3; // 8 bit number
	short y = 12; // 16 bit number
	int z = 123; // 32 bit number
	long a = 1234L; // 64 bit number
	float f = 1234.23f; // 32 bit number
	double d = 12.23; // 64 bit number
	boolean b = true; 
	char c = 'A'; // 16 bit unicode char

	int decimal = 1020;
	int octal = 0144; // 0 is the prefix for octal literals
	int hexadecimal = 0x64df; // 0x is the prefix for hexadecimal

	}
}
