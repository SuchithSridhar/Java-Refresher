public class primitiveDataTypes{
    public static void main(String[] args){
        // 8 bits
        byte byteType = 127;
        // 16 bits
        short shortType = 1122;
        // 32 bits
        int intType = 12939;
        // 64 bits
        long longType = 12345L;


        // 32 bits
        float floatType = 1.34f;
        // 64 bits
        double doubleType = 12.34;


        // 16 bits
        char charType = 'a';
        // 1 bit
        boolean boolType = true;
    }
}
